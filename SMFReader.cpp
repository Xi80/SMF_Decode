#include <iostream>
#include "SMFPlayer.h"

void midiCallback(uint8_t* data, size_t length) {
	if (length < 1)return;
	for (int i = 0; i < length; i++) {
		printf("%02X ", data[i]);
	}
	printf("\n");
}

int main()
{
	SMFPlayer player(midiCallback);

	int ret = player.Open("noname.mid");
	uint32_t delta = 0;

	while (player.Play(&delta) == SMF_RET_OK) {
		printf("Wait %d ms\n", delta);
	}
	std::cout << ret << "\n";
}

